#Exemplos de Inteiros
altura = 180  #Altura em centimetros
peso = 75  #Peso em quilogramas

#Operações com inteiros
soma = altura + peso  #Soma da altura e do peso juntos
print("Soma da altura e peso:", soma)

#Exemplos de números com casas decimais
preco = 19.99  #Preço de um produto
taxa = 0.05  #Taxa de juros
temperatura_media = 22.5  #Temperatura media em graus celsius

#Operaçôes com float
multiplicacao = preco * 2  #Custo de dois produtos
print("custo de dois produtos:", multiplicacao)

divisao = temperatura_media / 2  #Dividindo a temperatura media
print("Metade da temperatura média:", divisao)

#Exemplos de strings
